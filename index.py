    """–ó–∞–ø—É—Å–∫ –±–æ—Ç–∞"""
    
    # –ü—Ä–æ–≤–µ—Ä–∫–∞ –Ω–∞—Å—Ç—Ä–æ–µ–∫
    if BOT_TOKEN == "YOUR_BOT_TOKEN_HERE":
        print("‚ùå –û–®–ò–ë–ö–ê: –ù–µ —É–∫–∞–∑–∞–Ω BOT_TOKEN!")
        print("–û—Ç–∫—Ä–æ–π—Ç–µ —Ñ–∞–π–ª earnhub_bot.py –∏ —É–∫–∞–∂–∏—Ç–µ —Ç–æ–∫–µ–Ω –±–æ—Ç–∞.")
        return
    
    if ADMIN_ID == 1234567890:
        print("‚ö†Ô∏è –í–ù–ò–ú–ê–ù–ò–ï: –ù–µ —É–∫–∞–∑–∞–Ω ADMIN_ID!")
        print("–û—Ç–∫—Ä–æ–π—Ç–µ —Ñ–∞–π–ª earnhub_bot.py –∏ —É–∫–∞–∂–∏—Ç–µ –≤–∞—à Telegram ID.")
        return
    
    # –°–æ–∑–¥–∞–Ω–∏–µ –ø—Ä–∏–ª–æ–∂–µ–Ω–∏—è
    application = Application.builder().token(BOT_TOKEN).build()
    
